fun main() {
    val num01 = 127
    val num02 = -32786
    val num03 = 9999999999

    println("num01: $num01, num02: $num02, num03: $$num03")

    val exp01 = 3.14
    val exp02 = 3.14f
    val exp03 = 3.14e-2
    val exp04 = 3.14e2f

    println("ex01: $exp01, ex02:$exp02, ex03: $exp03, ex04: $exp04")
}